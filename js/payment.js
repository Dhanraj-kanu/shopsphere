/**
 * Show payment error modal
 * @param {String} errorMessage - Error message
 */
function showPaymentErrorModal(errorMessage) {
    const modal = document.createElement('div');
    modal.id = 'paymentErrorModal';
    modal.className = 'fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50';
    modal.innerHTML = `
        <div class="bg-white rounded-lg p-6 w-[90%] max-w-md">
            <div class="text-center">
                <div class="w-16 h-16 bg-red-100 rounded-full flex items-center justify-center mx-auto mb-4">
                    <i class="fas fa-times text-3xl text-red-500"></i>
                </div>
                <h3 class="text-xl font-bold mb-2">Payment Failed</h3>
                <p class="text-gray-600 mb-6">${errorMessage}</p>
                <div class="flex gap-3">
                    <button onclick="closePaymentModal()" 
                            class="flex-1 bg-gray-100 text-gray-700 py-3 rounded-lg font-semibold hover:bg-gray-200">
                        Close
                    </button>
                    <button onclick="closePaymentModal(); retryPayment()" 
                            class="flex-1 bg-green-500 text-white py-3 rounded-lg font-semibold hover:bg-green-600">
                        Try Again
                    </button>
                </div>
            </div>
        </div>
    `;
    document.body.appendChild(modal);
}

/**
 * Close payment modal
 */
function closePaymentModal() {
    const successModal = document.getElementById('paymentSuccessModal');
    const errorModal = document.getElementById('paymentErrorModal');
    if (successModal) successModal.remove();
    if (errorModal) errorModal.remove();
}

/**
 * Retry payment
 */
function retryPayment() {
    if (paymentOrderData) {
        initializeRazorpayPayment(paymentOrderData);
    }
}


