// Payment configuration
let paymentOrderData = null;

/**
 * Initialize Razorpay payment
 * @param {Object} orderData - Order details including amount, orderId, etc.
 */
function initializeRazorpayPayment(orderData) {
    paymentOrderData = orderData;

    const options = {
        key: RAZORPAY_KEY_ID,
        amount: orderData.payment.total * 100, // Amount in paise (multiply by 100)
        currency: 'INR',
        name: 'E-Commerce Store',
        description: `Order #${orderData.orderId}`,
        image: 'https://via.placeholder.com/150', // Your logo URL
        order_id: null, // This will be generated by your backend
        handler: function (response) {
            // Payment successful
            handlePaymentSuccess(response);
        },
        prefill: {
            name: orderData.shipping.fullName,
            email: orderData.shipping.email,
            contact: orderData.shipping.phone
        },
        notes: {
            address: `${orderData.shipping.address}, ${orderData.shipping.city}, ${orderData.shipping.state} ${orderData.shipping.postalCode}`,
            order_id: orderData.orderId
        },
        theme: {
            color: '#4CAF50'
        },
        modal: {
            ondismiss: function () {
                // User closed the payment modal
                handlePaymentFailure('Payment cancelled by user');
            }
        }
    };

    // For production, you should create an order on your backend first
    // and get the order_id from Razorpay. For now, we'll use direct payment
    const razorpay = new Razorpay(options);

    razorpay.on('payment.failed', function (response) {
        handlePaymentFailure(response.error.description || 'Payment failed');
    });

    razorpay.open();
}

/**
 * Handle successful payment
 * @param {Object} response - Razorpay payment response
 */
function handlePaymentSuccess(response) {
    // Update order data with payment information
    paymentOrderData.payment = {
        ...paymentOrderData.payment,
        razorpay_payment_id: response.razorpay_payment_id,
        razorpay_order_id: response.razorpay_order_id,
        razorpay_signature: response.razorpay_signature,
        status: 'paid',
        paid_at: new Date().toISOString()
    };

    // Complete the order
    completeOrder(paymentOrderData);

    // Customer notification: order placed & payment confirmed
    try {
        if (typeof window.addCustomerNotification === 'function') {
            window.addCustomerNotification({
                type: 'order',
                title: 'Order Confirmed',
                message: `Your order ${paymentOrderData.orderId} has been placed successfully.`,
                icon: 'fa-receipt',
                color: 'green'
            });
        }
    } catch (e) {
        console.error('Notification error:', e);
    }

    // Show success message
    showToast('Payment successful! Order placed.');

    // Show success modal
    showPaymentSuccessModal(paymentOrderData);
}

/**
 * Handle payment failure
 * @param {String} errorMessage - Error message
 */
function handlePaymentFailure(errorMessage) {
    console.error('Payment failed:', errorMessage);
    showToast(`Payment failed: ${errorMessage}`);

    // Show error modal
    showPaymentErrorModal(errorMessage);
}

/**
 * Complete the order after successful payment
 * @param {Object} orderData - Complete order data
 */
function completeOrder(orderData) {
    // Update order status to processing after successful payment
    orderData.status = 'processing';
    orderData.payment.status = 'paid';

    // Save order to localStorage
    const orders = JSON.parse(localStorage.getItem('orders')) || [];
    orders.unshift(orderData);
    localStorage.setItem('orders', JSON.stringify(orders));

    // Add notification for admin
    const notifications = JSON.parse(localStorage.getItem('notifications')) || [];
    notifications.unshift({
        message: `New paid order received: ${orderData.orderId} - ₹${orderData.payment.total.toFixed(2)}`,
        icon: 'fa-shopping-cart',
        timestamp: new Date().toISOString(),
        type: 'order'
    });
    localStorage.setItem('notifications', JSON.stringify(notifications));

    // Clear cart
    if (typeof resetCart === 'function') {
        resetCart();
    }

    // Update UI if on profile page
    if (typeof renderApp === 'function' && typeof currentPage !== 'undefined' && currentPage === 'profile') {
        setTimeout(() => {
            renderApp();
        }, 1000);
    }
}

